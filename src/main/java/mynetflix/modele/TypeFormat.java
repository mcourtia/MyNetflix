package mynetflix.modele;

public class TypeFormat {
	
	private int id;
	private String libelle;
	
	public TypeFormat(int id, String libelle) {
		super();
		this.id = id;
		this.libelle = libelle;
	}

	public int getId() {
		return id;
	}

	public String getLibelle() {
		return libelle;
	}
	
	
	

}
